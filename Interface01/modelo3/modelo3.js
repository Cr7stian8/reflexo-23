function comprar(){
    let tamanho = document.getElementById("tamanho");
    if(tamanho.value == "p"){
        window.location.href = "https://mpago.la/2F1sdiC";
    }
    else if(tamanho.value == "m"){
        window.location.href = "https://mpago.la/2YAsdeQ";
    }
    else{
        window.location.href = "https://mpago.la/1m7T2TM";
    }
}