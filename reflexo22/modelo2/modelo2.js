function comprar(){
    let tamanho = document.getElementById("tamanho");
    if(tamanho.value == "p"){
        window.location.href = "https://mpago.la/2MCoUEf";
    }
    else if(tamanho.value == "m"){
        window.location.href = "https://mpago.la/2LhzXZq";
    }
    else{
        window.location.href = "https://mpago.la/2z88mFa";
    }
}